public class Perfil {
    private int codigo;
    private String nome;

    public Perfil(){
        
    }
    public Perfil(String perfil) {
          this.nome = perfil;        
    }
    
    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
